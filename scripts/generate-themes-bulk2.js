// Generate dates from day after last existing
const lastDate = new Date(existing[existing.length - 1].date);
const newThemes = moreKeywords.map((kw, i) => {
  const d = new Date(lastDate);
  d.setDate(d.getDate() + i + 1);
  return {
    date: d.toISOString().split("T")[0],
    keyword: kw[0],
    desc: kw[1],
  };
});

